bool isGood(int one[], int zero[]) {
    if (one[0] > 0 and zero[0] > 0) return false;
    if (one[1] > 0 and zero[1] > 0) return false;
    if (one[0] > 0 and one[1] > 0) return false;
    if (zero[0] > 0 and zero[1] > 0) return false;
    return true;
}

void solve() {
    string s; cin >> s;
    int l = 0, ans = 0;
    int zero[2] = {0, 0}, one[2] = {0, 0};
    int n = s.size();
    for (int r = 0; r < n; r++) {
        if (s[r] == '1') {
            one[r % 2]++;
        } else if (s[r] == '0') {
            zero[r % 2]++;
        }
        while (!isGood(one, zero)) {
            if (s[l] == '1') {
                one[l % 2]--;
            } else if (s[l] == '0') {
                zero[l % 2]--;
            }
            l++;
        }
        ans += (r - l + 1);
    }
    cout << ans << endl;
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}