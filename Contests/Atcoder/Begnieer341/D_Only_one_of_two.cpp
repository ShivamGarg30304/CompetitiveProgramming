void solve() {
    int n, m, k;
    cin >> n >> m >> k;
    int lcm = (n * m) / __gcd(n, m);
    int ans = -1;
    int low = 0, high = 1e16;
    while (low <= high) {
        int sum = (low + high) / 2;
        int cnt1 = sum / low;
        int cnt2 = sum / high;
        int cnt3 = sum / lcm;
        int total = (cnt1 + cnt2 - cnt3 - 1);
        if (total > k) {
            high = sum - 1;
        }
        if (total < k) {
            low = sum + 1;
        }

    }
    int maxi = max(n, m) * k;
    int mini = min(n, m) * k;
    cout << maxi << " " << mini << endl;
    int c1 = mini / n;
    int c2 = mini / m;
    int c3 = mini / lcm;
    cout << c1 << " " << c2 << " " << c3 << endl;
    int cnt = c1 + c2 - c3;
    cout << cnt << endl;
    int rem = cnt - k;

    cout << maxi - max(n, m) * rem << endl;
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}