void solve() {
    string A, B;
    cin >> A >> B;
    int n = A.size(), m = B.size();
    vector<vector<int>> dp(n + 1, vector<int> (m + 1));
    dp[0][0] = 0;
    for (int i = 0; i <= n; i++) {
        dp[i][0] = i;
    }
    for (int j = 0; j <= m; j++) {
        dp[0][j] = j;
    }
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (A[i - 1] == B[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1];
            } else {
                int op1 = dp[i - 1][j - 1];
                int op2 = dp[i - 1][j];
                int op3 = dp[i][j - 1];
                dp[i][j] = min(op1, min(op2, op3)) + 1;
            }
        }
    }
    cout << dp[n][m] << endl;
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}